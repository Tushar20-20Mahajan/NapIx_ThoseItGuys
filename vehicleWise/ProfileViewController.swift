import UIKit

class ProfileViewController: UIViewController , UITableViewDelegate, UITableViewDataSource {
       
        
     
       
        
@IBOutlet weak var tableViewOfAccountDisplayingInformation: UITableView!
        
        
    override func viewDidLoad() {
        super.viewDidLoad()
            
        tableViewOfAccountDisplayingInformation.dataSource = self
        tableViewOfAccountDisplayingInformation.delegate = self
        tableViewOfAccountDisplayingInformation.isScrollEnabled = false
            
            
        tableViewOfAccountDisplayingInformation.register(UITableViewCell.self, forCellReuseIdentifier: "accountCell")
            
    }
            
            // Profile Infomation profilesSectionOne
            
    func numberOfSections(in tableView: UITableView) -> Int {
                return 1
    }
            
            
            
    func tableView(_ tableView: UITableView, numberOfRowsInSection section: Int) -> Int {
            return profilesSectionOne.count
    }
        
    func tableView(_ tableView: UITableView, cellForRowAt indexPath: IndexPath) -> UITableViewCell {
            let cell = tableView.dequeueReusableCell(withIdentifier: "accountCell", for: indexPath)
            let profile = profilesSectionOne[indexPath.row]
            cell.textLabel?.text = profile.title
            cell.imageView?.image = profile.image
            cell.accessoryType = .disclosureIndicator
            return cell
    }
            
    func tableView(_ tableView: UITableView, didSelectRowAt indexPath: IndexPath) {
                if let selectedIndexPath = tableView.indexPathForSelectedRow {
                    tableView.deselectRow(at: selectedIndexPath, animated: true)
                }
                
                tableView.selectRow(at: indexPath, animated: true, scrollPosition: .none)
    }
            
            
}

